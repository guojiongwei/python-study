# 循环list
print('----循环list----')
list = (1, '2', True)
for i in list:
    print(i)

# 循环元组
print('----循环元组----')
tuple = (1, '2', True)
for i in tuple:
    print(i)

# 循环字符串
print('----循环字符串----')
str = '12345'
for i in str:
    print(i)

# 循环字典
print('----循环字典----')
dict = {'a': 1, 'b': 2, 'c': 3}
for i in dict:
    print(i)
    print(dict[i])

# 循环集合
print('----循环集合----')
set = {1, 2, 3}
for i in set:
    print(i)

# 循环range
print('----循环range----')
for i in range(1, 10):
    print(i)
